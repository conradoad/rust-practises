fn main() {
    muttable();
    shaddowing();
}

fn muttable() {
    println!("Exemple 1");
    let mut x = 5;
    println!("The value of x is: {x}");
    x = 6;
    println!("The value of x is: {x}");
}

fn shaddowing() {
    println!("Exemple 2");
    let x = 5;

    let x = x + 1;

    {
        let x = x * 2;
        println!("The value of x in the inner scope is: {x}");
    }

    println!("The value of x is: {x}");
}
